                  <p>From a techincal standpoint I decided to move the hosting of the site from AWS to a managed WordPress host. This move to a dedicated and managed WordPress host will provide increased security and free up resources as it would take the constant monitoring and updating of a WP site away from the in-house DevOps team.</p>
